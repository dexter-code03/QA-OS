# Publishing
# ═════════════════════════════════════════════════

def publish_report(report_text: str, metrics: dict) -> str:
    """Publish the sign-off report to Confluence.

    Args:
        report_text: Full report narrative.
        metrics: Cycle metrics for summary panel.

    Returns:
        URL of the published Confluence page.
    """
    print("📄 Publishing to Confluence...")
    confluence = ConfluenceClient()

    version = metrics["fix_version"]
    date = metrics["date"]
    title = f"QA Sign-off Report — {version} ({date})"

    test_exec = metrics.get("test_execution", {})
    bugs = metrics.get("bugs", {})
    summary = (
        f"Pass rate: <strong>{test_exec.get('pass_rate', 0)}%</strong> | "
        f"Open bugs: <strong>{bugs.get('open', 0)}</strong> | "
        f"Critical: <strong>{bugs.get('critical', 0)}</strong>"
    )

    url = confluence.publish_report(
        title=title,
        report_text=report_text,
        metrics_summary=summary,
    )

    print(f"   → Published: {url}")
    return url


def notify_slack(metrics: dict, report_url: str, verdict: str) -> None:
    """Post the sign-off digest card to Slack.

    Args:
        metrics: Cycle metrics.
        report_url: URL of the Confluence report.
        verdict: Sign-off verdict string.
    """
    print("💬 Posting Slack digest...")
    slack = SlackNotifier()

    test_exec = metrics.get("test_execution", {})
    bugs = metrics.get("bugs", {})

    slack.post_digest_card(
        fix_version=metrics["fix_version"],
        pass_rate=test_exec.get("pass_rate", 0),
        open_bugs=bugs.get("open", 0),
        critical_bugs=bugs.get("critical", 0),
        resolved_bugs=bugs.get("resolved", 0),
        flaky_tests=test_exec.get("flaky", 0),
        verdict=verdict,
        report_url=report_url,
    )


# ═════════════════════════════════════════════════
# Full Pipeline
# ═════════════════════════════════════════════════

def run_signoff_pipeline(
    sprint_id: str,
    fix_version: str,
    publish: bool = True,
) -> dict:
    """Run the complete QA sign-off pipeline.

    1. Collect metrics from Katalon, Jira, Confluence
    2. Generate report narrative via Claude
    3. Publish to Confluence
    4. Post Slack digest card
    5. Return verdict

    Args:
        sprint_id: Sprint identifier.
        fix_version: Release version string.
        publish: Whether to publish to Confluence and Slack.

    Returns:
        dict with keys: metrics, report, verdict, confluence_url
    """
    print("\n" + "=" * 60)
    print("🚀 Stage 7: QA Sign-off Report Pipeline")
    print("=" * 60)

    # Step 1: Collect metrics
    metrics = collect_cycle_metrics(sprint_id, fix_version)

    # Step 2: Generate report
    report = generate_report(metrics)

    # Step 3: Extract verdict
    verdict = extract_verdict(report)
    print(f"\n📋 Verdict: {verdict}")

    result = {
        "metrics": metrics,
        "report": report,
        "verdict": verdict,
        "confluence_url": None,
    }

    if publish:
        # Step 4: Publish to Confluence
        try:
            result["confluence_url"] = publish_report(report, metrics)
        except Exception as e:
            print(f"   ⚠️  Confluence publish failed: {e}")

        # Step 5: Post to Slack
        try:
            notify_slack(
                metrics,
                result["confluence_url"] or "Report URL unavailable",
                verdict,
            )
        except Exception as e:
            print(f"   ⚠️  Slack notification failed: {e}")

    # Save report locally as well
    reports_dir = Path(config.REPORTS_DIR)
    reports_dir.mkdir(parents=True, exist_ok=True)
    local_path = reports_dir / f"signoff_{fix_version}_{metrics['date']}.md"
    local_path.write_text(report, encoding="utf-8")
    print(f"💾 Report saved locally: {local_path}")

    print(f"\n✅ Sign-off pipeline complete!")
    return result


