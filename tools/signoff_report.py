def collect_katalon_metrics(sprint_id: str) -> dict:
    """Fetch test execution metrics from Katalon TestOps API.

    Args:
        sprint_id: Sprint identifier for filtering results.

    Returns:
        dict with total, passed, failed, flaky, pass_rate, flaky_details.
    """
    print("📊 Collecting Katalon TestOps metrics...")

    if not config.KATALON_API_KEY:
        print("   ⚠️  Katalon API key not configured — using placeholder metrics")
        return {
            "total": 0, "passed": 0, "failed": 0, "flaky": 0,
            "pass_rate": 0, "flaky_details": [],
        }

    try:
        headers = {"Authorization": f"Bearer {config.KATALON_API_KEY}"}
        resp = requests.get(
            f"{config.KATALON_TESTOPS_URL}/api/v1/test-runs",
            headers=headers,
            params={
                "projectId": config.KATALON_PROJECT_ID,
                "sprintId": sprint_id,
            },
        )
        resp.raise_for_status()
        data = resp.json()

        total = data.get("totalTests", 0)
        passed = data.get("passedTests", 0)
        failed = data.get("failedTests", 0)
        flaky = data.get("flakyTests", 0)
        pass_rate = round((passed / total) * 100, 1) if total else 0

        return {
            "total": total,
            "passed": passed,
            "failed": failed,
            "flaky": flaky,
            "pass_rate": pass_rate,
            "flaky_details": data.get("flakyTestDetails", []),
        }

    except Exception as e:
        print(f"   ⚠️  Katalon API error: {e}")
        return {
            "total": 0, "passed": 0, "failed": 0, "flaky": 0,
            "pass_rate": 0, "flaky_details": [],
        }


def collect_jira_metrics(fix_version: str) -> dict:
    """Fetch bug metrics from Jira for the given fix version.

    Args:
        fix_version: Release version string (e.g. "v2.3.0").

    Returns:
        dict with bug counts by severity and status.
    """
    print("🐛 Collecting Jira bug metrics...")
    jira = JiraClient()

    try:
        jql = (
            f'project = {config.JIRA_PROJECT_KEY} '
            f'AND issuetype = {config.JIRA_BUG_ISSUE_TYPE} '
            f'AND fixVersion = "{fix_version}"'
        )
        bugs = jira.search_issues(jql, max_results=200)

        result = {
            "total": len(bugs),
            "critical": 0,
            "major": 0,
            "minor": 0,
            "trivial": 0,
            "resolved": 0,
            "open": 0,
            "deferred": 0,
            "bugs_detail": [],
        }

        for bug in bugs:
            fields = bug.get("fields", {})
            priority = fields.get("priority", {}).get("name", "Medium")
            status = fields.get("status", {}).get("name", "Open")
            labels = [l.lower() for l in fields.get("labels", [])]

            if priority in ("Highest", "Critical"):
                result["critical"] += 1
            elif priority == "High":
                result["major"] += 1
            elif priority in ("Medium", "Low"):
                result["minor"] += 1
            else:
                result["trivial"] += 1

            if status == "Done":
                result["resolved"] += 1
            elif "deferred" in labels:
                result["deferred"] += 1
            else:
                result["open"] += 1

            result["bugs_detail"].append({
                "key": bug["key"],
                "summary": fields.get("summary", ""),
                "priority": priority,
                "status": status,
                "component": (
                    fields.get("components", [{}])[0].get("name", "")
                    if fields.get("components") else ""
                ),
            })

        print(f"   → {result['total']} bugs found ({result['critical']} critical, {result['open']} open)")
        return result

    except Exception as e:
        print(f"   ⚠️  Jira API error: {e}")
        return {
            "total": 0, "critical": 0, "major": 0, "minor": 0,
            "trivial": 0, "resolved": 0, "open": 0, "deferred": 0,
            "bugs_detail": [],
        }


def collect_cycle_metrics(sprint_id: str, fix_version: str) -> dict:
    """Collect all metrics for a test cycle from all sources.

    Args:
        sprint_id: Sprint identifier.
        fix_version: Release version string.

    Returns:
        Combined metrics dict for report generation.
    """
    katalon_metrics = collect_katalon_metrics(sprint_id)
    jira_metrics = collect_jira_metrics(fix_version)

    # Try to get FRD content for coverage analysis
    frd_text = ""
    if config.FRD_PAGE_ID:
        try:
            confluence = ConfluenceClient()
            frd_text = confluence.get_page_body_text(config.FRD_PAGE_ID)
            print(f"📄 FRD content loaded ({len(frd_text)} chars)")
        except Exception as e:
            print(f"   ⚠️  Could not load FRD: {e}")

    return {
        "sprint_id": sprint_id,
        "fix_version": fix_version,
        "date": datetime.now().strftime("%Y-%m-%d"),
        "test_execution": katalon_metrics,
        "bugs": jira_metrics,
        "frd_text": frd_text,
    }


# ═════════════════════════════════════════════════
# Report Generation
# ═════════════════════════════════════════════════

def generate_report(metrics: dict) -> str:
    """Use Claude to write the full QA sign-off report narrative.

    Args:
        metrics: Combined metrics from collect_cycle_metrics().

    Returns:
        Complete report text with all 7 sections.
    """
    print("✍️  Generating report...")

    # Prepare metrics summary
    metrics_for_ai = {
        "sprint_id": metrics["sprint_id"],
        "fix_version": metrics["fix_version"],
        "date": metrics["date"],
        "test_execution": metrics["test_execution"],
        "bugs": {
            k: v for k, v in metrics["bugs"].items()
            if k != "bugs_detail"
        },
        "open_bugs_detail": [
            b for b in metrics["bugs"].get("bugs_detail", [])
            if b.get("status") != "Done"
        ][:20],
    }

    content = f"Generate the QA sign-off report for this cycle:\n\n{json.dumps(metrics_for_ai, indent=2)}"

    # Include FRD excerpt for coverage analysis
    frd = metrics.get("frd_text", "")
    if frd:
        content += f"\n\nFRD content for coverage analysis (excerpt):\n{frd[:4000]}"

    report = ai_chat(REPORT_PROMPT, content)
    print(f"   → Report generated ({len(report)} chars)")
    return report


def extract_verdict(report_text: str) -> str:
    """Extract the sign-off verdict from the report text.

    Returns:
        Verdict string (e.g. "✅ APPROVED FOR RELEASE").
    """
    for line in report_text.splitlines():
        stripped = line.strip()
        if any(v in stripped for v in ["✅", "⚠️", "❌"]):
            if any(k in stripped.upper() for k in ["APPROVED", "NOT APPROVED"]):
                return stripped
    return "Verdict not found in report"


